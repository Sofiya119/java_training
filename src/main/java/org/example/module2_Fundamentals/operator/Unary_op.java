package org.example.module2_Fundamentals.operator;

public class Unary_op {
    public static void main(String[] args) {
        int a = -10;
        System.out.println(a);
        int b = +10;
        System.out.println(b);
        ++b;
        System.out.println(b);
        --b;
        System.out.println(b);

    }
}
